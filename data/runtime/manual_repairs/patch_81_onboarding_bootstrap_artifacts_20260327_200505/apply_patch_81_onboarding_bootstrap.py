        f"- **Wrapper root:** `{wrapper_root}`",
        f"- **Runtime path:** `{runtime_path or '(default profile runtime)'}`",
        f"- **Project packet:** `{resolved_packet_path}`",
        f"- **Current stage:** {current_stage}",
        "",
        "## 2. Suggested reading order",
        "1. README.md",
