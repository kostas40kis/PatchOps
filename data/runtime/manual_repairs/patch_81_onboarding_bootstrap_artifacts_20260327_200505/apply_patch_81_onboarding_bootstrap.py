def ensure_project_packets_block(text: str) -> str:
    if PROJECT_PACKETS_MARKER_START in text and PROJECT_PACKETS_MARKER_END in text:
        prefix = text.split(PROJECT_PACKETS_MARKER_START, 1)[0]
        suffix = text.split(PROJECT_PACKETS_MARKER_END, 1)[1]
        return prefix.rstrip() + "\n\n" + PROJECT_PACKETS_BLOCK.strip() + "\n" + suffix.lstrip("\n")
    return text.rstrip() + "\n\n" + PROJECT_PACKETS_BLOCK.strip() + "\n"

def ensure_cli_parser(text: str) -> str:
    if CLI_MARKER_START in text:
        return text
    needle = "\n\n    return parser\n"
    if needle not in text:
        raise RuntimeError("Could not find parser return block in patchops/cli.py.")
    return text.replace(needle, "\n\n" + CLI_BLOCK.rstrip() + "\n\n    return parser\n", 1)

def ensure_cli_dispatch(text: str) -> str:
    if CLI_MARKER_DISPATCH in text:
        return text
    needle = '\n\n    parser.error("Unknown command")\n'
    if needle not in text:
        needle = '\n\n    parser.error(\\"Unknown command\\")\n'
    if needle not in text:
        raise RuntimeError("Could not find Unknown command fallback in patchops/cli.py.")
    return text.replace(needle, "\n\n" + CLI_DISPATCH_BLOCK.rstrip() + "\n\n    parser.error(\"Unknown command\")\n", 1)

def main() -> int:
    import argparse

    parser = argparse.ArgumentParser()
    parser.add_argument("--project-root", required=True)
    args = parser.parse_args()

    root = Path(args.project_root).resolve()

    project_packets_path = root / "patchops" / "project_packets.py"
    cli_path = root / "patchops" / "cli.py"
    test_path = root / "tests" / "test_project_packet_onboarding_bootstrap.py"

    project_packets_text = project_packets_path.read_text(encoding="utf-8")
    project_packets_text = ensure_project_packets_block(project_packets_text)
    project_packets_path.write_text(project_packets_text, encoding="utf-8")

    cli_text = cli_path.read_text(encoding="utf-8")
    cli_text = ensure_cli_parser(cli_text)
    cli_text = ensure_cli_dispatch(cli_text)
    cli_path.write_text(cli_text, encoding="utf-8")

    test_path.write_text(TEST_FILE, encoding="utf-8")

    print(
        json.dumps(
            {
                "written_files": [
                    str(project_packets_path),
                    str(cli_path),
                    str(test_path),
                ]
            },
            indent=2,
        )
    )
    return 0

if __name__ == "__main__":
    raise SystemExit(main())